print("Hello World")
print("Hello Reader")
# If we run this program both the print statements appears in new line.
# To merge view both the statements in same line we use end=' '
print("Hello World", end=' ')
print("Hello Reader")
# We can do this without using end=' '
print("Hello World", "Hello Reader")
# We can use anything in the place of space( ).
print("Hello World", end=' Ram ')
print("Hello Reader")
