#include <iostream>
using namespace std;
int main()
{
    // Comparison Operators
    int a = 4, b = 5;
    cout << "The value of a == b " << (a == b) << endl;
    cout << "The value of a != b " << (a != b) << endl;
    cout << "The value of a >= b " << (a >= b) << endl;
    cout << "The value of a <= b " << (a <= b) << endl;
    cout << "The value of a > b " << (a > b) << endl;
    cout << "The value of a < b " << (a < b) << endl;
    return 0;

}
