#include <iostream>
using namespace std;
int main()
{
    // Arithmetic Operators
    int a = 12, b = 3;
    cout << "The value of a + b is " << a + b << endl;
    cout << "The value of a - b is " << a - b << endl;
    cout << "The value of a * b is " << a * b << endl;
    cout << "The value of a / b is " << a / b << endl;
    cout << "The value of a % b is " << a % b << endl;
    cout << "The value of a++ is " << a++ << endl; // Prints a then add 1
    cout << "The value of ++a is " << ++a << endl; // Add 1 then print a
    cout << "The value of a-- is " << a-- << endl; // Prints a then sub 1
    cout << "The value of --a is " << --a << endl; // Sub 1 then print a
    return 0;
}
