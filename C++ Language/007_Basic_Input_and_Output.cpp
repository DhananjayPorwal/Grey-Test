#include <iostream>
using namespace std;
int main()
{
    int a, b;
    cout << "Enter the value of a" << endl;
    // Here "<<" represents Insertion Operator
    cin >> a;
    // Here ">>" represents Extraction Operator
    cout << "Enter the value of b" << endl;
    cin >> b;
    cout << "The sum of a and b is " << a + b << endl;

    return 0;
}
