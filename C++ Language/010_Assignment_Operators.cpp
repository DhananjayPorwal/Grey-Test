#include <iostream>
using namespace std;
int main()
{
    // Assignment Operators
    int a = 6, b = 5;
    cout << a << endl;
    b += a;
    cout << b << endl;
    b -= a;
    cout << b << endl; 
    b *= a;
    cout << b << endl; 
    b /= a;
    cout << b << endl;
    b %= a;
    cout << b << endl; 

}
